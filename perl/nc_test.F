!BOP
!
! !IROUTINE: nc_test
!
! !DESCRIPTION: Routine to test the Fortran code that is automatically
!  generated by the ncCodeDef, ncCodeWrite, and ncCodeRead scripts.
!\\
!\\
! !INTERFACE:

      PROGRAM NC_TEST
!
! !REMARKS:
!  Assumes that you have:
!  (1) A netCDF library (either v3 or v4) installed on your system
!  (2) The NcdfUtilities package (from Bob Yantosca) source code
!
! !REVISION HISTORY:
!  30 Jan 2012 - R. Yantosca - Initial version
!EOP
!------------------------------------------------------------------------------
!BOC
!
! !LOCAL VARIABLEs
!
      INTEGER :: fId ! netCDF file ID

      ! Define netCDF variables & attributes
      ! (code generated by ncCodeDef)
      CALL DEFINE_NETCDF_FILE( fId )

      ! Write data to netCDF file
      ! (code generated by ncCodeWrite)
      CALL WRITE_TO_NETCDF_FILE( fId )

      ! Read data back from netCDF file
      ! (code generated by ncCodeRead)
      CALL READ_FROM_NETCDF_FILE( fId )
      
      END PROGRAM NC_TEST
!EOC
